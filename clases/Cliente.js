class Cliente {
    constructor(id, nombre, apellidos, edad, email, telefono, direccion, usuario, contrasena, fechaDeRegistro) {
        this.id = id;
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
        this.email = email;
        this.telefono = telefono;
        this.direccion = direccion;
        this.usuario = usuario;
        this.contrasena = contrasena;
        this.fechaDeRegistro = fechaDeRegistro;
    }
}