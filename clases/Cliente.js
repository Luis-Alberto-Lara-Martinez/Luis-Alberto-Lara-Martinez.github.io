class Cliente {
    constructor(id, nombre, edad, email, telefono, direccion, usuario, contrasena) {
        this.id = id;
        this.nombre = nombre;
        this.edad = edad;
        this.email = email;
        this.telefono = telefono;
        this.direccion = direccion;
        this.usuario = usuario;
        this.contrasena = contrasena;
    }
}